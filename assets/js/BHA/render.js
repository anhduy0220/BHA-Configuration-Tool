/* render.js
   Phase 2: UI Rendering Logic
   - This script handles the rendering of the output generated by the rule engine.
   Phase 3: Re-Render all outcomes per parameter
   - Uses the output keys and the dictionary to display recommendations and explanations to the user.
   - Highlights the recommended options from rules.js output.
   - Renders into the output area on the right column.
*/

(() => {
  const SHOW_DEBUG_KEY = false;

  function createEl(tag, className = "", attrs = {}) {
    const el = document.createElement(tag);
    if (className) el.className = className;
    for (const [k, v] of Object.entries(attrs)) {
      if (v === null || v === undefined) continue;
      el.setAttribute(k, String(v));
    }
    return el;
  }

  function safeArray(value) {
    return Array.isArray(value) ? value : [];
  }

  function getDictionary() {
    const dict = window.BHA_DICTIONARY;
    if (!dict) {
      console.warn("[render.js] BHA_DICTIONARY not found. Did you load dictionary.js first?");
      return null;
    }
    return dict;
  }

  function getMasterTable() {
    const mt = window.BHA_MASTER_TABLE;
    if (!mt) {
      console.warn("[render.js] BHA_MASTER_TABLE not found. Update dictionary.js to include the master layout.");
      return null;
    }
    return mt;
  }

  function buildSelectedSet(output) {
    const keysObj = output?.keys || {};
    const selected = new Set();

    safeArray(keysObj.Tools).forEach((k) => selected.add(k));
    safeArray(keysObj.Motors).forEach((k) => selected.add(k));
    safeArray(keysObj.Bit).forEach((k) => selected.add(k));

    return selected;
  }

  // Row items
  function buildRow(item, key, selectedSet) {
    const selected = selectedSet.has(key);

    // Highlight any selected outcome (recommended output)
    const row = createEl(
      "div",
      `bha-master-row${selected ? " is-selected" : ""}`,
      { "data-key": key }
    );

    // Title only (compact like your screenshot)
    const title = createEl("div", "bha-master-row__title");
    title.textContent = item.title || key;
    row.appendChild(title);

    // Optional link (kept compact)
    if (item.link && item.link !== "#") {
      const link = createEl("a", "bha-master-row__link", {
        href: item.link,
        target: "_blank",
        rel: "noopener noreferrer",
      });
      link.textContent = "Learn more";
      row.appendChild(link);
    }

    if (SHOW_DEBUG_KEY) {
      const debug = createEl("div", "bha-master-row__debug");
      debug.textContent = key;
      row.appendChild(debug);
    }

    return row;
  }

  function renderColumn(colName, sections, dict, selectedSet) {
    const col = createEl("div", "bha-master-col");
    col.style.minWidth = "0";

    const head = createEl("div", "bha-master-col__head");
    head.textContent = colName;
    col.appendChild(head);

    const body = createEl("div", "bha-master-col__body");

    sections.forEach((sec) => {
      const secWrap = createEl("div", "bha-master-sec");

      const secHead = createEl("div", "bha-master-sec__head");
      secHead.textContent = sec.section;
      secWrap.appendChild(secHead);

      const secBody = createEl("div", "bha-master-sec__body");

      sec.keys.forEach((key) => {
        const item = dict[key] || dict.UNKNOWN_OUTPUT_KEY || { title: key, link: "#" };
        secBody.appendChild(buildRow(item, key, selectedSet));
      });

      secWrap.appendChild(secBody);
      body.appendChild(secWrap);
    });

    col.appendChild(body);
    return col;
  }

  function renderEmpty(outputArea) {
    const box = createEl("div", "text-center text-secondary py-5");
    box.innerHTML = `
      <div class="mb-2 fw-semibold">No master table layout found</div>
      <div class="small">Check dictionary.js to ensure BHA_MASTER_TABLE is defined.</div>
    `;
    outputArea.appendChild(box);
  }

  window.renderOutputs = function renderOutputs(output) {
    const outputArea = document.getElementById("outputArea");
    if (!outputArea) {
      console.warn('[render.js] #outputArea not found. Add <div id="outputArea"></div> to your right column.');
      return;
    }

    const dict = getDictionary();
    const master = getMasterTable();
    if (!dict || !master) return renderEmpty(outputArea);

    const selectedSet = buildSelectedSet(output);

    outputArea.innerHTML = "";

    // Main wrapper
    const wrap = createEl("section", "bha-master-table");
    const grid = createEl("div", "bha-master-grid"); // CSS will set 3 columns

    grid.appendChild(renderColumn("Tools", master.Tools || [], dict, selectedSet));
    grid.appendChild(renderColumn("Motors", master.Motors || [], dict, selectedSet));
    grid.appendChild(renderColumn("Bit", master.Bit || [], dict, selectedSet));

    wrap.appendChild(grid);
    outputArea.appendChild(wrap);
  };
})();